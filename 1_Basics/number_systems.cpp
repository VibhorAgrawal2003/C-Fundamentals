#include<iostream>

using namespace std;

int main(){

    int number_1 = 12; // Decimal representation
    int number_2 = 0b101; // Binary representation
    int number_3 = 013; // Octal representation
    int number_4 = 0x11A; // Hexadecimal representation

    cout << "number_1: " << number_1 << endl;
    cout << "number_2: " << number_2 << endl;
    cout << "number_3: " << number_3 << endl;
    cout << "number_4: " << number_4 << endl; 

    return 0;
}